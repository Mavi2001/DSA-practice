#include<bits/stdc++.h>
using namespace std;
int main()
{
    vector<int>nums = {2,5,6,0,0,1,2};
    int target = 0;
    
    
    int count=0;
    
    for(int i=0;i<nums.size();i++)
    {
        if(target == nums[i]);
        count+=1;
    }
    if(count==0)
    cout<<"false"<<endl;
    else
    cout<<"true"<<endl;
    
}